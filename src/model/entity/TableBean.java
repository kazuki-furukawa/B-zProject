package model.entity;

public class TableBean {

	int tableNum;

	int gameValue;

	int east;
	int south;
	int west;
	int north;

	String startTime;
	String endTime;
	/**
	 * @return tableNum
	 */
	public int getTableNum() {
		return tableNum;
	}
	/**
	 * @param tableNum セットする tableNum
	 */
	public void setTableNum(int tableNum) {
		this.tableNum = tableNum;
	}
	/**
	 * @return east
	 */
	public int getEast() {
		return east;
	}
	/**
	 * @param east セットする east
	 */
	public void setEast(int east) {
		this.east = east;
	}
	/**
	 * @return south
	 */
	public int getSouth() {
		return south;
	}
	/**
	 * @param south セットする south
	 */
	public void setSouth(int south) {
		this.south = south;
	}
	/**
	 * @return west
	 */
	public int getWest() {
		return west;
	}
	/**
	 * @param west セットする west
	 */
	public void setWest(int west) {
		this.west = west;
	}
	/**
	 * @return north
	 */
	public int getNorth() {
		return north;
	}
	/**
	 * @param north セットする north
	 */
	public void setNorth(int north) {
		this.north = north;
	}
	/**
	 * @return startTime
	 */
	public String getStartTime() {
		return startTime;
	}
	/**
	 * @param startTime セットする startTime
	 */
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	/**
	 * @return endTime
	 */
	public String getEndTime() {
		return endTime;
	}
	/**
	 * @param endTime セットする endTime
	 */
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	/**
	 * @return gameValue
	 */
	public int getGameValue() {
		return gameValue;
	}
	/**
	 * @param gameValue セットする gameValue
	 */
	public void setGameValue(int gameValue) {
		this.gameValue = gameValue;
	}


}
